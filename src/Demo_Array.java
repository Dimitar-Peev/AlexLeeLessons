import java.util.Arrays;

public class Demo_Array {
    public static void main(String[] args) {

        String[] fruits = new String[4];

        fruits[0] = "Mango";
        fruits[1] = "Apple";
        fruits[2] = "Strawberry";
        fruits[3] = "Watermelon";

        System.out.println(Arrays.toString(fruits));
    }
}
